class LDA_Pointer:

    def __init__(self, year, category_num):
        self.year = year
        self.cat_num = category_num

    def set_year(self, yaer):
        self.year = yaer

    def get_year(self):
        return self.year
    
    def set_category_num(self, cat_num):
        self.cat_num = cat_num
    
    def get_category_num(self):
        return self.cat_num

